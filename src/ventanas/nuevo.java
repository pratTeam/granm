    private void FOKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_FOKeyTyped
        if(!operadorFlag){
            if(Character.isDigit(evt.getKeyChar()) || evt.getKeyChar() == '.'){ 
            }
            else{
                evt.consume();
            }
            operadorFlag = false;
        }
        else{
            if(evt.getKeyChar() == '+' || evt.getKeyChar() == '-'){ 
                operadorFlag = false;
            }
            else{
                evt.consume();
            }
        }
    }//GEN-LAST:event_FOKeyTyped

